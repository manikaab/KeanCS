package Unit_1_Loops_and_Methods;

public class Switch_Case 
{
	public static void main(String[] args) 
	{
		int day = 1;
		
		switch(day) 
		{
			case 0: 
				System.out.println("Sunday");
			break; 
			
			case 1: System.out.println("Monday");
			break; 		
			
			default: System.out.println("No Day");
			break; 
		}
	}
}
